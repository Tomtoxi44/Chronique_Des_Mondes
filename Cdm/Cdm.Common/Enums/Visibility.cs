namespace Cdm.Common.Enums;

/// <summary>
/// Represents the visibility level of a campaign
/// </summary>
public enum Visibility
{
    /// <summary>
    /// Campaign is private and only visible to invited players
    /// </summary>
    Private = 0,

    /// <summary>
    /// Campaign is public and visible to all users
    /// </summary>
    Public = 1
}
